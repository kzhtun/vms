public class CondoInfo {

    private String CondoName;
    private String HomePagePhoto;
    private String LogInPhoto;
    private String TransPhoto;

    public String getCondoName() {
        return CondoName;
    }

    public void setCondoName(String CondoName) {
        this.CondoName = CondoName;
    }

    public String getHomePagePhoto() {
        return HomePagePhoto;
    }

    public void setHomePagePhoto(String HomePagePhoto) {
        this.HomePagePhoto = HomePagePhoto;
    }

    public String getLogInPhoto() {
        return LogInPhoto;
    }

    public void setLogInPhoto(String LogInPhoto) {
        this.LogInPhoto = LogInPhoto;
    }

    public String getTransPhoto() {
        return TransPhoto;
    }

    public void setTransPhoto(String TransPhoto) {
        this.TransPhoto = TransPhoto;
    }
}
